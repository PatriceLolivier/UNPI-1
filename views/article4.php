                        <h4>BAUX D’HABITATION :</h4>
                        <div class="section-heading-line-left"></div><br>
                        <p>
                            Malgré sa ferme opposition, la possibilité pour des collectivités d’instaurer un « permis de louer » est inscrite dans notre droit depuis la loi ALUR de 2014. Et elles sont de plus en plus nombreuses à le faire.
                            L’UNPI tente cependant de faire annuler les délibérations locales qui détournent le permis de louer de l’objectif poursuivi par le législateur : instauration dans un secteur où il y a une faible proportion d’habitat
                            dégradé, abus des contrôles exercés sur les propriétaires, etc.
                        </p>
                        <p>
                            A la suite d’une action devant le tribunal administratif de l’UNPI 36 et de l’UNPI Val-de-Loire avec le soutien de la fédération UNPI, Châteauroux-Métropole a annulé
                            la première version du permis de louer qu’elle avait adoptée à l’automne 2020 et l’a remplacée par un permis de louer recentré sur le cœur historique de la Ville. Les
                            logements de moins de quinze ans sont également exemptés de permis de louer.
                            D’autres actions sont prévues sur l’ensemble du territoire. L’UNPI a par ailleurs solennellement demandé à la Ministre du Logement de contrôler davantage, via le déféré préfectoral,
                            l’instauration du permis de louer dans de nombreuses communes de notre territoire.
                            Afin de limiter les abus, certaines Chambres UNPI ont opté pour une démarche constructive ; en travaillant avec les instances locales, elles espèrent faire évoluer les réglementations dans
                            l’intérêt des propriétaires comme des locataires.
                        </p>

                        <h4>Report d’expulsions locatives : l’UNPI veille à ce que les propriétaires soient correctement indemnisés</h4>
                        <div class="section-heading-line-left"></div> <br>

                        <p>
                            L’UNPI milite inlassablement pour que les préfets prêtent rapidement main forte à l’exécution des jugements d’expulsion. Hélas, les retards s’accumulent en raison du souhait de l’Etat de subordonner toute expulsion à
                            une solution préalable de relogement. Par ailleurs, la trêve hivernale a été prolongée de plusieurs mois en 2020 et en 2021 à cause de l’épidémie de Covid-19.
                            L’UNPI demande à tout le moins que les propriétaires soient correctement indemnisés en cas de retard dans l’exécution des jugements d’expulsion. Par ailleurs, si le report de la trêve hivernale peut se justifier par le
                            contexte sanitaire, ce report ne doit pas être mis à la charge des propriétaires.
                        </p> <br>

                        <p>
                            Dans une instruction du 26 avril 2021, Emmanuelle Wargon, Ministre chargée du Logement, et Marlène Schiappa, Ministre chargée de la Citoyenneté, rappellent
                        </p> <br>
                        <p>
                            l’engagement de l’Etat « à indemniser rapidement tous les propriétaires impactés de l’intégralité des sommes légalement exigibles ». L’instruction annonce
                            d’ailleurs l’octroi de crédits supplémentaires aux préfets pour « instruire sans attendre l’ensemble des demandes d’indemnisation ».
                            Enfin, les ministres confirment dans l’instruction que l’indemnisation doit couvrir la période de report exceptionnel de la trêve hivernale en raison de la crise sanitaire.
                        </p> <br>

                        <h3>FISCALITE :</h3> <br>

                        <h4>Réforme du dispositif « Louer abordable » (autrement appelé « Cosse»)</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>Le dispositif « Cosse » permet à des propriétaires bailleurs en zone « tendue » de bénéficier de déductions fiscales entre 15 % et 70 %
                            (85 % en cas d’intermédiation locative) s’ils respectent des plafonds de loyer (et de ressources des locataires) et à condition de signer une convention avec l’Anah.
                            L’UNPI critique depuis longtemps la lourdeur du dispositif (il implique notamment des engagements d’au moins six ans) et
                            son illisibilité (des plafonds de loyer sont publiés au niveau national, mais les délégations territoriales de l’Anah peuvent les moduler). L’UNPI critique également
                            le fait que, lorsque la convention prend fin et que le bien est reloué,
                            le propriétaire ne puisse généralement pas revenir à un loyer de marché (en raison du blocage des loyers de relocation en zone tendue).
                        </p><br>

                        <p>
                            Lors d’une conférence de presse du 15 novembre 2021, la ministre en charge du Logement, Emmanuelle Wargon, a déploré la perte de vitesse du dispositif actuel. Elle a présenté les contours d’un nouveau dispositif, plus clair et plus attractif. Ce dispositif est inscrit dans le projet de loi de finances pour 2022, tel qu’adopté par l’Assemblée nationale en première lecture le 16 novembre 2021, à la suite d’un amendement déposé par le Gouvernement.
                            Dans le cadre du dispositif en projet les déductions fiscales seraient remplacées par des réductions d’impôt, applicables sur le montant global de l’impôt dû. L’avantage en euros serait donc le même pour les contribuables concernés quel que soit leur taux marginal d’imposition (sous réserve toutefois d’être « imposables »).
                            Le taux de la réduction serait de 15 % (20 % en intermédiation locative) en cas de loyer inférieur à 15 % au loyer de marché, de 35 % (40 % en intermédiation locative) en cas de loyer inférieur au loyer de marché, et de 65 % en cas de décote de 45 % par rapport au loyer de marché (uniquement en intermédiation locative). A la différence d’aujourd’hui, il n’y aurait pas de modulation des taux de réduction en fonction d’un zonage (actuellement, le taux des déductions varie suivant que le logement se situe en zone A, Abis, B1 ou B2)
                            Les plafonds de loyer, aujourd’hui fixés par les délégations locales de l’Anah, seraient désormais déterminés par décret à partir des loyers de marché considérés à l’échelle communale…
                            Enfin, et ce serait une grande avancée, les premières relocations après la fin de la convention signée avec l’Anah ne seraient plus soumises au décret annuel de blocage des loyers (sauf pour les logements F et G) !
                        </p> <br>
                        <p>
                            Ces mesures sont de nature à augmenter l’attrait du dispositif pour les propriétaires. Cependant, l’effort de simplification doit être poursuivi. Par ailleurs, l’UNPI s’interroge sur la façon dont les futurs « loyers de marché »
                            servant de référence aux plafonds de loyer seront fixés. Enfin, l’UNPI continue de militer pour le maintien de l’avantage fiscal tant que le locataire reste en place avec le même loyer (à ce jour, le maintien de l’avantage fiscal n’est
                            acquis que jusqu’au renouvellement ou la reconduction tacite du bail postérieurs à l’échéance de la convention Anah).
                        </p>

                        <h4>UN ralentissement de la hausse des taxes foncières</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p> L’UNPI a créé il y a quinze ans l’Observatoire national des taxes foncières. Cet observatoire est devenu aujourd’hui la référence pour connaitre précisément l’évolution des taxes foncières en France.
                            En dénonçant chaque année le dérapage de la taxe foncière, l’UNPI contribue à une prise de conscience
                        </p> <br>
                        <p>
                            Sur une période de dix ans, l’augmentation moyenne de la taxe foncière est toujours très forte (quasiment + 28 % entre 2010 et 2020). Mais la hausse provient essentiellement de la première partie de la décennie. En cinq ans, la hausse de taxe foncière ralentit :
                            + 11,4 % entre 2015 et 2020, contre 12,1 % entre 2014 et 2019, 16,4 % entre 2009 et 2014 ou encore 21,2 % entre 2007 et 2012.
                            Des villes ont réalisé un effort de modération, comme Nice (augmentation de 15 % entre 2010 et 2020 qui ne résulte que de la revalorisation des valeurs locatives) qui a signé un pacte de stabilité avec l’UNPI en 2019.
                        </p> <br>

                        <p>
                            L’UNPI continue de dénoncer le report de certaines parts manquantes de taxe d’habitation sur les résidences principales (en cours de suppression) sur les taxes foncières (part au profit des syndicats de communes, Gemapi, etc.).
                            Elle sera également très vigilante sur la refonte complète des valeurs locatives d’ici 2026. A ce sujet, elle a écrit au Ministre chargé des Comptes publics pour exiger que les propriétaires et leurs représentants soient officiellement intégrés dans les commissions chargées de fixer les futurs « tarifs ».
                        </p>
                        <h3>BAUX COMMERCIAUX :</h3>
                        <h4>Résidences services : l’UNPI à la manœuvre pour défendre les propriétaires en résidences</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            L’UNPI à travers sa structure dédiée, conseille juridiquement, accompagne et informe les propriétaires bailleurs sur la complexité du statut des baux commerciaux auquel ils sont soumis.
                            Elle milite inlassablement pour que le rapport de force entre les exploitants, preneurs à bail, et les propriétaires bailleurs soient rééquilibrés. En effet, bien que les exploitants aient perçu des aides, nombreux sont les propriétaires qui font toujours l’objet d’impayés de loyers.
                            L’UNPI demande à tout le moins, une transparence sur l’utilisation des aides perçues par les exploitants et que ces dernières servent effectivement au paiement des loyers.
                        </p> <br>

                        <p>
                            Dans un courrier en date du 7 avril 2021 à Madame Kuster, députée de Paris, M. Bruno LEMAIRE, ministre de l’Économie, a indiqué qu’« une réforme visant à améliorer l’information précontractuelle des investisseurs en résidence de tourisme, afin de mieux avertir des risques liés à ce type d’investissement (…) » était à l’étude. Le ministère de l’économie, des finances et de la relance a d’ailleurs confirmé à M. Robert Thérry, député et rapporteur de l’avis sur le projet de loi de finances pour 2022 concernant le tourisme (auquel l’UNPI a contribué) « qu’un projet de réforme visant à améliorer l’information précontractuelle pour les particuliers investisseurs était à l’étude ».
                            A ce titre, l’UNPI a fait part à M. Bruno LEMAIRE, ministre de l’Économie, de ses travaux sur la proposition d’une adaptation du statut des baux commerciaux aux résidences de tourisme afin de mieux protéger les investisseurs et de la nécessité de rendre la relation entre exploitants et propriétaires plus transparente et équilibrée.
                        </p>

                        <h4>Crédit d’impôt pour les loyers commerciaux ou professionnels abandonnés</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            Après les fermetures administratives de l’automne 2020, le Gouvernement a annoncé son intention d’inscrire dans la loi de finances pour 2021 un crédit d’impôt pour les remises de loyer consenties par les propriétaires. Il a d’abord été indiqué que ce crédit d’impôt serait fixé à 30 % du loyer mensuel abandonné.
                            L’UNPI a réclamé que ce taux soit porté au moins à 50 % afin que la charge supportée par les propriétaires ne soit pas trop importante. Elle a également demandé que l’abandon de loyer puisse être consenti après le 1er janvier 2021.
                        </p> <br>

                        <p>
                            L’article 20 de la loi de finances pour 2021 prévoit un crédit d’impôt correspondant à 50 % du loyer mensuel abandonné. De plus, sont éligibles les propriétaires louant à des enseignes de plus de 250 salariés (le crédit d’impôt correspond alors à 50 % des deux tiers d’un loyer mensuel, le locataire étant censé continuer de payer un tiers de loyer).
                            L’abandon du loyer de novembre 2020 peut être consenti jusqu’au 31 décembre 2021 (le crédit d’impôt est alors imputé sur l’impôt dû au titre de 2021).
                            Rappelons que ce crédit d’impôt est couplé à la défiscalisation des loyers remisés, également obtenue par l’UNPI.

                        </p> <br>

                        <h3>TRANSITION ENERGETIQUE :</h3>
                        <h4>Des corrections apportées au DPE nouvelle version</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            L’UNPI a rapporté au Gouvernement les nombreuses anomalies suscitées par le nouveau DPE, applicable depuis le 1er juillet 2021.
                        </p><br>

                        <p>
                            Fin septembre 2021, le Gouvernement a suspendu l’édition de nouveaux DPE pour les logements construits avant 1975 dans l’attente de correctifs. Les DPE déjà réalisés pourront être corrigés, <i> « sans frais supplémentaire pour les propriétaires immobiliers »</i> 1.

                            https://www.ecologie.gouv.fr/diagnostic-performance-energetique-direction-generale-lamenagement-du-logement-et-nature-recommande
                        </p> <br>

                        <p>
                            Le 4 octobre 2021, Christophe Demerson, président de l’UNPI, a défendu auprès de la Ministre du Logement plusieurs correctifs.
                        </p> <br>

                        <p>
                            Un arrêté du 8 octobre 2021 modifiant la méthode de calcul et les modalités d'établissement du DPE a été publié.
                            L’UNPI demeure extrêmement mobilisée sur le sujet des DPE. Elle critique l’adoption brutale d’un calendrier qui provoquera la sortie du marché locatif de nombreux logements n’atteignant pas les classes énergétiques exigées. Elle préconise de nombreuses mesures d’accompagnement : franchise fiscale des aides à la rénovation énergétique, création d’un congé au locataire pour travaux d’économie d’énergie, gel sinon abattement de taxe foncière pour les propriétaires réalisant des travaux, etc.
                            L’UNPI a diffusé une pétition à envoyer au Premier Ministre. C’est par la mobilisation massive de tous que la voix des propriétaires pourra être entendue !

                        </p>

                        <h3>A suivre par ailleurs :</h3>
                        <h4>Encadrement des loyers</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            Après Paris et Lille, l’encadrement des loyers est progressivement étendu à plusieurs communes de la couronne parisienne (Saint-Denis, Aubervilliers, etc.), Lyon, ou encore Bordeaux…
                            L’UNPI a engagé des recours judiciaires pour contester la légitimité de cet encadrement dans ces villes ou critiquer les plafonds fixés par le préfet.
                        </p><br>

                        <h4>Cautionnement Visale</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            Comme le préconisait l’UNPI, la caution Visale peut désormais être mobilisée pour tous les locataires de plus de trente ans ayant un salaire inférieur ou égal à 1500€ nets par mois.
                        </p>

                        <h4>Squats de logements</h4>
                        <div class="section-heading-line-left"></div><br>

                        <p>
                            L’UNPI dénonce l’insuffisance des nouvelles lois qui tentent péniblement de mettre un terme aux phénomènes de squats (notamment loi ASAP du 7 décembre 2020 précisant le rôle des préfets en cas de violation du domicile d’autrui).
                            L’UNPI appuie les divers projets de réforme visant à protéger efficacement toutes les propriétés (pas seulement les logements) contre les squats.
                        </p>
