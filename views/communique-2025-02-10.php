                <h3 class="mt-4 mb-3">Loi de finances pour 2025 : des mesures insuffisantes pour les propriétaires</h3>

                <p>Après le rejet de la motion de censure à l'Assemblée nationale, le Sénat a adopté définitivement le projet de loi de finances (PLF) pour 2025. Beaucoup accueillent avec satisfaction ce vote transpartisan de mesures importantes pour les Français.</p>

                <p>Rappelons toutefois que la promulgation de la loi demeure suspendue à la décision du Conseil constitutionnel, saisi par plus de 60 députés.</p>

                <p>Sur le fond, <strong>l’UNPI déplore une vision déséquilibrée et lacunaire de la politique du logement</strong> et constate que la majorité des mesures adoptées se concentre uniquement sur le marché du neuf, laissant de côté les logements existants.</p>

                <h3 class="mt-4 mb-3">Les mesures adoptées</h3>

                <ul>
                    <li><strong>Augmentation des droits de mutation à titre onéreux (DMTO) :</strong> Les départements pourront augmenter les frais de notaire de 0,5 point pour trois ans. Exonération prévue pour les primo-accédants.</li>
                    <li><strong>Extension du prêt à taux zéro (PTZ) :</strong> Étendu à l’achat de logements neufs sur tout le territoire, y compris zones rurales. Le PTZ dans l’existant reste sous conditions de travaux.</li>
                    <li><strong>Exonération des donations familiales :</strong> Donations jusqu’à 100 000 € exonérées si le bien est occupé pendant au moins cinq ans.</li>
                    <li><strong>Réduction du budget de Ma Prime Rénov’ :</strong> Baisse de 460 millions d’euros, risquant de freiner la rénovation énergétique.</li>
                    <li><strong>Aménagement de la fiscalité des locations meublées non professionnelles (LMNP) :</strong> Assouplissement de l’article 24 du PLF. Les résidences de tourisme restent concernées.</li>
                    <li><strong>Prorogation du dispositif Loc’ Avantages jusqu’en 2027 :</strong> Encouragement de la location à loyers modérés contre avantages fiscaux.</li>
                </ul>

                <h3 class="mt-4 mb-3">À propos de l’UNPI</h3>

                <p>L’UNPI, association loi 1901, œuvre depuis plus de 130 ans pour représenter, accompagner et défendre les intérêts des propriétaires privés, petits ou grands. Avec plus de 100 000 adhérents et représentant plus de 700 000 logements, l’UNPI repose sur l’engagement de ses 76 chambres locales réparties sur tout le territoire.</p>

                <p>Seul acteur local et national au service de la propriété, l’UNPI défend les propriétaires privés auprès des pouvoirs publics. Chaque mois, sa revue <strong>« 35 millions de propriétaires »</strong>, portée par l’UNPI, informe avec des contenus de qualité entièrement dédiés à la propriété.</p>

